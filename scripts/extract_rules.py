def clean_pdf_text(text):
    """PDFから抽出したテキストをクリーンアップ"""
    # Remove control characters (except newline and tab)
    text = re.sub(r'[\x00-\x08\x0b\x0c\x0e-\x1f\x7f]', '', text)
    # Remove InDesign artifacts like "JOEE", "ɺংจ" etc
    text = re.sub(r'[ɺ][^\s]{0,20}', '', text)
    text = re.sub(r'JOEE?\b', '', text)
    text = re.sub(r'A5_[^\n]*', '', text)
    text = re.sub(r'\bJOEE\b', '', text)
    text = re.sub(r'ଇॻ\$[^\n]*', '', text)
    text = re.sub(r'͍ํ[^\n]*', '', text)
    # Remove page number artifacts at end of lines
    text = re.sub(r'\s*\d{1,3}\s*$', '', text, flags=re.MULTILINE)
    # Remove excessive whitespace
    text = re.sub(r'[ \t]+', ' ', text)
    text = re.sub(r'\n{3,}', '\n\n', text)
    # Remove lines that are just numbers (page numbers)
    text = re.sub(r'^\s*\d{1,3}\s*$', '', text, flags=re.MULTILINE)
    return text.strip()


def extract_rule_content(pages, rules):
    """PDFページテキストから各規則のコンテンツを抽出 (ページ単位で処理)"""
    # Build page-indexed text for better section detection
    # Skip first ~12 pages (cover, TOC, preface) and last ~20 pages (definitions, index)
    rule_pages = pages[12:270]  # Main rule content pages
    full_text = "\n".join([p["text"] for p in rule_pages])
    full_text = clean_pdf_text(full_text)

    # Build a flat list of all subsection numbers to find boundaries
    all_subs = []
    for rule in rules:
        for sub in rule["subsections"]:
            all_subs.append(sub["number"])

    # For each subsection, find its content
    for rule in rules:
        for i, sub in enumerate(rule["subsections"]):
            sub_num = sub["number"]
            # More specific pattern - subsection number at meaningful position
            # e.g., "18.2 " not just "182"
            pattern = r'(?:^|\n)\s*' + re.escape(sub_num) + r'\s'
            matches = list(re.finditer(pattern, full_text))

            if not matches:
                # Try alternative pattern
                pattern2 = re.escape(sub_num) + r'\s+\S'
                matches = list(re.finditer(pattern2, full_text))

            if matches:
                # Use the first substantial match (skip TOC references)
                best_match = None
                for m in matches:
                    # Check if this is a content section (not just a TOC reference)
                    after_text = full_text[m.start():m.start() + 200]
                    # TOC entries are typically short lines with page numbers
                    if len(after_text.split('\n')[0]) > 30:
                        best_match = m
                        break
                if not best_match and matches:
                    best_match = matches[-1]  # Use last match as fallback

                if best_match:
                    start = best_match.start()

                    # Find end: next subsection number or limit
                    end = start + 3000  # Max content per subsection

                    # Find the next subsection in the flat list
                    sub_idx = all_subs.index(sub_num) if sub_num in all_subs else -1
                    if sub_idx >= 0 and sub_idx + 1 < len(all_subs):
                        next_num = all_subs[sub_idx + 1]
                        next_pattern = r'(?:^|\n)\s*' + re.escape(next_num) + r'\s'
                        next_matches = list(re.finditer(next_pattern, full_text[start + 20:]))
                        if next_matches:
                            candidate_end = start + 20 + next_matches[0].start()
                            end = min(end, candidate_end)

                    content = full_text[start:end].strip()
                    content = clean_pdf_text(content)

                    # Truncate at a reasonable point
                    if len(content) > 2000:
                        # Try to end at a sentence
                        cutoff = content.rfind('。', 0, 2000)
                        if cutoff > 500:
                            content = content[:cutoff + 1]
                        else:
                            content = content[:2000] + '...'

                    sub["content"] = content

    return rules


